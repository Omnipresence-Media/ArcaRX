export function getInvoicesByPatient(patientId: string) {
  return invoices.filter((i) => i.patientId === patientId).sort((a, b) => b.date.localeCompare(a.date));
}

export function getPatientBalance(patientId: string) {
  return invoices.filter((i) => i.patientId === patientId).reduce((sum, i) => sum + i.balance, 0);
}
